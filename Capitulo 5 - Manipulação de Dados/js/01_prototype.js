/*
    Prototype : protótipo, ou aquilo que veio antes.

    JS : é uma linguagem baseada em protótipos, algo que se assemelha ao conceito de OOP.

    __proto__ : propriedade padrão do objeto. Dentro dele há uma série de propriedades e funcionalidades.

    Por exemplo, um objeto vazio herda uma série de características e métodos do objeto padrão : __proto__
*/


console.log("Felippe".__proto__);
console.log(20.0.__proto__);
