/*
    Operadores de Atribuição (Assignment)
    x = 0

    = : igual simples / x = 1
    +=: acréscimo / x = x+1 = 1
    -=: decrécimo / x = x-1 = -1
    *=: automultiplicação / x = x * 1 = 0
    /=: autodivisão / x = x / 1 = (indeterminado) 


*/

let x;
console.log(x);


//Assignment 
x = 1;
console.log(x);

//Atribuição e adicao
x+=2; // x = x + 2 = 3
console.log(x);

//Atribuição e subtração
x-=2; // x = x - 2 = 1
console.log(x)

//Atribuição e múlti
x*=2; //x = x * 2 = 2
console.log(x)

//Atribuição e divisão
x/=2; //x = x / 2 = 1
console.log(x)




