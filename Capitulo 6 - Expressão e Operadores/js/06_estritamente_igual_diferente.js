/*
    Operadores estritamente igual a / estritamente diferente 

    === : Compara os valores e o tipo de dado
    !== : Compara os valores e verifica se são diferentes em valores e tipo

*/

const n1 = 1;
const n2 = 2;


console.log(n1==1);//true 
console.log(n1==="1"); // false : tipo de dado diferente


console.log(n2!==2); // false. São valores iguais
console.log(n2!=="2");// true. Porque o tipo de dado é diferente
