/*
    Operadores condicionais 

    ==: igual
    !=: Diferente
    > : Maior que
    < : Menor que
    >=: Maior ou igual
    <=: Menor igual que

*/

const n1 = 1;
const n2 = 2;


console.log(n1>1);//false : iguais
console.log(n1>=1); // true : iguais

console.log(n1 < n2); //true : menor
console.log(n1 > n2);//false : menor

console.log(n1<=0); // false : maior
console.log(n1<0); // falso : maior
console.log(n1<=1); // true : igual


